export async function generateUsername(
	credentialId: string,
	salt = 0,
): Promise<string> {
	const input = salt > 0 ? `${credentialId}:${salt}` : credentialId;
	const data = new TextEncoder().encode(input);
	const hash = await crypto.subtle.digest("SHA-256", data);
	const bytes = new Uint8Array(hash);

	const adjIdx = bytes[0];
	const nounIdx = bytes[1];
	const num = ((bytes[2] << 8) | bytes[3]) % 10000;

	const adj = ADJECTIVES[adjIdx % ADJECTIVES.length];
	const noun = NOUNS[nounIdx % NOUNS.length];

	return `${adj}-${noun}-${num.toString().padStart(4, "0")}`;
}

export async function generateUniqueUsername(
	credentialId: string,
	db: D1Database,
): Promise<string> {
	for (let salt = 0; salt < 10; salt++) {
		const username = await generateUsername(credentialId, salt);
		const existing = await db
			.prepare("SELECT 1 FROM users WHERE username = ?")
			.bind(username)
			.first();
		if (!existing) return username;
	}
	// Fallback: use random suffix
	const bytes = new Uint8Array(4);
	crypto.getRandomValues(bytes);
	const suffix = Array.from(bytes)
		.map((b) => b.toString(16).padStart(2, "0"))
		.join("");
	return `user-${suffix}`;
}

